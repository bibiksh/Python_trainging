def star(n):
    for i in range(n,0,-1):
        print((n-i)*" "+i*"*")
#star(10)


def star2(n):
    alist=[i for i in range(n,0,-1)]
    for count,v in enumerate(alist):
        print(count*" "+v*"*")
star2(100)